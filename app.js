// Adds two numbers
function add(a, b) {
    return a + b;
}

// Subtracts two numbers
function subtract(a, b) {
    return a - b;
}

// Multiplies two numbers
function multiply(a, b) {
    return a * b;
}

// Divides two numbers
function divide(a, b) {
    return a / b;
}

function operate(operator, a, b) {
    operator(a,b);
}